// Package main 是 ent 代码生成的入口。
//
// 运行：go generate ./ent
package main

import (
	"log"
	"os"

	"entgo.io/ent/entc"
	"entgo.io/ent/entc/gen"
	"entgo.io/ent/schema/field"
)

func main() {
	if err := entc.Generate("./schema", &gen.Config{
		// Hub 自己的 ent，避免污染 sub2api 的 ent
		Target: "./ent",
		Package: "github.com/stifer/agent-hub/ent",
		Header: "// Code generated by ent, DO NOT EDIT.\n",
		IDType: field.TypeInt64,
		Features: []gen.Feature{
			gen.FeatureUpsert,
			gen.FeatureIntercept,
		},
	}, entc.TemplateFiles("...")); err != nil {
		log.Fatalf("running ent codegen: %v", err)
		os.Exit(1)
	}
}
